package com.wanma.ims.controller.vo;

/**
 * Created by xyc on 2018/3/20.
 * 数据中心使用的订单VO类
 */
public class DataCenterOrderVO {

    private Long provinceCode;

    private Long cityCode;

    private Long cpyNum;

    private String code;

    private Long chargeTime;//充电时长，单位毫秒

    private String chargeNum;//充电电量，保留两位小数，*100

    public Long getProvinceCode() {
        return provinceCode;
    }

    public void setProvinceCode(Long provinceCode) {
        this.provinceCode = provinceCode;
    }

    public Long getCityCode() {
        return cityCode;
    }

    public void setCityCode(Long cityCode) {
        this.cityCode = cityCode;
    }

    public Long getCpyNum() {
        return cpyNum;
    }

    public void setCpyNum(Long cpyNum) {
        this.cpyNum = cpyNum;
    }

    public Long getChargeTime() {
        return chargeTime;
    }

    public void setChargeTime(Long chargeTime) {
        this.chargeTime = chargeTime;
    }

    public String getChargeNum() {
        return chargeNum;
    }

    public void setChargeNum(String chargeNum) {
        this.chargeNum = chargeNum;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
