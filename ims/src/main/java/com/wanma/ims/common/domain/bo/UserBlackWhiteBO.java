package com.wanma.ims.common.domain.bo;

/**
* <p>Description: 黑白名单</p>
* @author bingo
* @date 2017-7-5
 */
public class UserBlackWhiteBO {

	private String userAccount;// 用户帐号
 
	private Integer type; //类型 0.白名单 1.黑名单
	
	private int flag;  //标识单桩或渠道
	
	private String electricPileCode;//桩编码

	public String getUserAccount() {
		return userAccount;
	}

	public void setUserAccount(String userAccount) {
		this.userAccount = userAccount;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public int getFlag() {
		return flag;
	}

	public void setFlag(int flag) {
		this.flag = flag;
	}

	public String getElectricPileCode() {
		return electricPileCode;
	}

	public void setElectricPileCode(String electricPileCode) {
		this.electricPileCode = electricPileCode;
	}
}
