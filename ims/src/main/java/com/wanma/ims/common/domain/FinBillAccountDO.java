package com.wanma.ims.common.domain;

import com.wanma.ims.common.domain.base.BasicModel;


/**
 * 账单科目
 * @author bingo
 * @date 2017-06-13
 */
public class FinBillAccountDO extends BasicModel{

	private static final long serialVersionUID = -6489550267529476700L;
	
	/** 主键 */
	private Long pkId;
	
	/** 账单科目编码  */
	private String billAccountCode;
	
	/** 账单科目   */
	private String billAccountName;
	
	/** 是否删除 0.否 1.删除 */
	private Integer isDel;
	
	public Long getPkId() {
		return pkId;
	}

	public void setPkId(Long pkId) {
		this.pkId = pkId;
	}

	public String getBillAccountCode() {
		return billAccountCode;
	}
	
	public void setBillAccountCode(String billAccountCode) {
		this.billAccountCode = billAccountCode;
	}
	
	public String getBillAccountName() {
		return billAccountName;
	}
	
	public void setBillAccountName(String billAccountName) {
		this.billAccountName = billAccountName;
	}
	
	public Integer getIsDel() {
		return isDel;
	}
	
	public void setIsDel(Integer isDel) {
		this.isDel = isDel;
	}
}