package com.wanma.ims.common.domain;

import java.util.List;

import com.wanma.ims.common.domain.base.BasicModel;

/**
 * 菜单MeunDO
 */
public class MenuDO extends BasicModel {

	private static final long serialVersionUID = -8855032796679657161L;

	// 菜单ID
	private String menuId;
	// 菜单分类：1菜单权限，2按钮权限
	private String menuType;
	// 显示内容
	private String contents;
	// 排序
	private Long sortNumber;
	// 链接
	private String url;
	// 画面显示标签
	private String rel;
	// 父菜单ID
	private String parentMenuId;
	// 备注
	private String notes;
	private String roleId;
	private String isSelected;
	private String parentMenuName;

	/** 菜单角色权限列表 */
	private List<RoleDO> roleList;
	private List<String> parentMenuIds;

	public String getMenuId() {
		return menuId;
	}

	public void setMenuId(String menuId) {
		this.menuId = menuId;
	}

	public String getMenuType() {
		return menuType;
	}

	public void setMenuType(String menuType) {
		this.menuType = menuType;
	}

	public String getContents() {
		return contents;
	}

	public void setContents(String contents) {
		this.contents = contents;
	}

	public Long getSortNumber() {
		return sortNumber;
	}

	public void setSortNumber(Long sortNumber) {
		this.sortNumber = sortNumber;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getParentMenuId() {
		return parentMenuId;
	}

	public void setParentMenuId(String parentMenuId) {
		this.parentMenuId = parentMenuId;
	}

	public String getRel() {
		return rel;
	}

	public void setRel(String rel) {
		this.rel = rel;
	}

	public String getNotes() {
		return notes;
	}

	public void setNotes(String notes) {
		this.notes = notes;
	}

	public String getParentMenuName() {
		return parentMenuName;
	}

	public void setParentMenuName(String parentMenuName) {
		this.parentMenuName = parentMenuName;
	}

	public List<RoleDO> getRoleList() {
		return roleList;
	}

	public void setRoleList(List<RoleDO> roleList) {
		this.roleList = roleList;
	}

	public List<String> getParentMenuIds() {
		return parentMenuIds;
	}

	public void setParentMenuIds(List<String> parentMenuIds) {
		this.parentMenuIds = parentMenuIds;
	}

	public String getRoleId() {
		return roleId;
	}

	public void setRoleId(String roleId) {
		this.roleId = roleId;
	}

	public String getIsSelected() {
		return isSelected;
	}

	public void setIsSelected(String isSelected) {
		this.isSelected = isSelected;
	}

}
