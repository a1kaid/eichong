package com.wanma.ims.common.domain;

import com.wanma.ims.common.domain.base.BasicModel;

/**
 * 日志
 */
public class LogCommitDO extends BasicModel{

	private static final long serialVersionUID = 4355743048274310043L;
	//主键id
	private String logId;
	//用户名称
    private String logName;
    //ip地址
    private String logIpAddress;
    //操作内容
    private String logContent;
	//用户来源
    private Long creatorId;
    
	public String getLogId() {
		return logId;
	}
	
	public void setLogId(String logId) {
		this.logId = logId;
	}
	
	public String getLogName() {
		return logName;
	}
	
	public void setLogName(String logName) {
		this.logName = logName;
	}
	
	public String getLogIpAddress() {
		return logIpAddress;
	}
	
	public void setLogIpAddress(String logIpAddress) {
		this.logIpAddress = logIpAddress;
	}
	
	public String getLogContent() {
		return logContent;
	}
	
	public void setLogContent(String logContent) {
		this.logContent = logContent;
	}

	public Long getCreatorId() {
		return creatorId;
	}
	
	public void setCreatorId(Long creatorId) {
		this.creatorId = creatorId;
	}

	@Override
	public String toString() {
		return "LogCommitDO [logId=" + logId + ", logName=" + logName + ", logIpAddress=" + logIpAddress + ", logContent=" + logContent + ",creatorId="+creatorId+", toString()=" + super.toString() + "]";
	}
	
	
}
