package com.wanma.model;

import java.util.Date;

import com.bluemobi.product.model.common.BasicListAndMutiFile;

/**
 * 反馈表
 * 
 * @version V1.0
 * @author Administrator
 * @date 2014年8月14日
 */
public class Feedback extends BasicListAndMutiFile{

	/**
	 * serial Version UID
	 */
	private static final long serialVersionUID = -946545956537136412L;

	/** 反馈ID */
	private String feedbackId;
	/** 用户ID */
	private String userId;
	/** 内容 */
	private String content;
	/** 处理状态 */
	private String processStatus;

	// 以下字段不予数据库对应
	private String start_create_date;
		
	private String end_create_date;
	
	private String phone;
	
	private String userIname;
	
	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getUserIname() {
		return userIname;
	}

	public void setUserIname(String userIname) {
		this.userIname = userIname;
	}

	public String getStart_create_date() {
		return start_create_date;
	}

	public void setStart_create_date(String start_create_date) {
		this.start_create_date = start_create_date;
	}

	public String getEnd_create_date() {
		return end_create_date;
	}

	public void setEnd_create_date(String end_create_date) {
		this.end_create_date = end_create_date;
	}

	/**
	 * 反馈ID的取得。
	 * 
	 * @return 反馈ID
	 */
	public String getFeedbackId() {
		return feedbackId;
	}

	/**
	 * 反馈ID的设定。
	 * 
	 * @param pFeedbackId
	 *            反馈ID
	 */
	public void setFeedbackId(String pFeedbackId) {
		this.feedbackId = pFeedbackId;
	}

	/**
	 * 用户ID的取得。
	 * 
	 * @return 用户ID
	 */
	public String getUserId() {
		return userId;
	}

	/**
	 * 用户ID的设定。
	 * 
	 * @param pUserId
	 *            用户ID
	 */
	public void setUserId(String pUserId) {
		this.userId = pUserId;
	}

	/**
	 * 内容的取得。
	 * 
	 * @return 内容
	 */
	public String getContent() {
		return content;
	}

	/**
	 * 内容的设定。
	 * 
	 * @param pContent
	 *            内容
	 */
	public void setContent(String pContent) {
		this.content = pContent;
	}

	/**
	 * 处理状态的取得。
	 * 
	 * @return 处理状态
	 */
	public String getProcessStatus() {
		return processStatus;
	}

	/**
	 * 处理状态的设定。
	 * 
	 * @param pProcessStatus
	 *            处理状态
	 */
	public void setProcessStatus(String pProcessStatus) {
		this.processStatus = pProcessStatus;
	}

}
