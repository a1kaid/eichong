package com.wanma.dubbox.model;


import java.util.Date;

import com.wanma.dubbox.model.common.BasicModel;


/**
 * 
 * tbl_FeedBack表
 * @author lhy
 *
 */
public class TblFeedback extends BasicModel {
	private static final long serialVersionUID = 1L;
	private String txt; // 反馈内容
	private Integer uid; // 用户ID tbl_User_normal_view中获取
	private String sts; // 状态
	private String rsn; // 原因
	private Integer udtUid; // 修改人
	public String getTxt() {
		return txt;
	}
	public void setTxt(String txt) {
		this.txt = txt;
	}
	public Integer getUid() {
		return uid;
	}
	public void setUid(Integer uid) {
		this.uid = uid;
	}
	public String getSts() {
		return sts;
	}
	public void setSts(String sts) {
		this.sts = sts;
	}
	public String getRsn() {
		return rsn;
	}
	public void setRsn(String rsn) {
		this.rsn = rsn;
	}
	public Integer getUdtUid() {
		return udtUid;
	}
	public void setUdtUid(Integer udtUid) {
		this.udtUid = udtUid;
	}
}