package com.wanma.dubbox.model;

import java.io.Serializable;
import java.util.Date;

import com.wanma.dubbox.model.common.BasicModel;

public class TblActivity extends BasicModel implements Serializable {
	private static final long serialVersionUID = 3112793700180198885L;

	private String name;

	private Integer tp;

	private Integer chTp;

	private String sts;

	private Integer rule;

	private String cuid;

	private String opUid;

	private String remark;

	private Date bgDate;

	private Date edDate;


	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getTp() {
		return tp;
	}

	public void setTp(Integer tp) {
		this.tp = tp;
	}

	public Integer getChTp() {
		return chTp;
	}

	public void setChTp(Integer chTp) {
		this.chTp = chTp;
	}

	public String getSts() {
		return sts;
	}

	public void setSts(String sts) {
		this.sts = sts;
	}

	public Integer getRule() {
		return rule;
	}

	public void setRule(Integer rule) {
		this.rule = rule;
	}

	public String getCuid() {
		return cuid;
	}

	public void setCuid(String cuid) {
		this.cuid = cuid;
	}

	public String getOpUid() {
		return opUid;
	}

	public void setOpUid(String opUid) {
		this.opUid = opUid;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public Date getBgDate() {
		return bgDate;
	}

	public void setBgDate(Date bgDate) {
		this.bgDate = bgDate;
	}

	public Date getEdDate() {
		return edDate;
	}

	public void setEdDate(Date edDate) {
		this.edDate = edDate;
	}

}