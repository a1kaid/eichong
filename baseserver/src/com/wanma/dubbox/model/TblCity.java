package com.wanma.dubbox.model;

import java.math.BigDecimal;
import java.util.Date;

import com.wanma.dubbox.model.common.BasicModel;

/**
 * 
 * tbl_City表
 * 
 * @author mew
 *
 */
public class TblCity extends BasicModel {
	private static final long serialVersionUID = -1890471814425772114L;
	private String name; // 城市名称
	private String creUser; // 创建时间
	private Date udate; // 修改时间
	private String upUser; // 修改人
	private BigDecimal sevLmt;
	private String htFlg;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCreUser() {
		return creUser;
	}

	public void setCreUser(String creUser) {
		this.creUser = creUser;
	}

	public Date getUdate() {
		return udate;
	}

	public void setUdate(Date udate) {
		this.udate = udate;
	}

	public String getUpUser() {
		return upUser;
	}

	public void setUpUser(String upUser) {
		this.upUser = upUser;
	}

	public BigDecimal getSevLmt() {
		return sevLmt;
	}

	public void setSevLmt(BigDecimal sevLmt) {
		this.sevLmt = sevLmt;
	}

	public String getHtFlg() {
		return htFlg;
	}

	public void setHtFlg(String htFlg) {
		this.htFlg = htFlg;
	}
}