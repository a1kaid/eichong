package com.wanma.dubbox.model;

import java.io.Serializable;
import java.util.Date;

import com.wanma.dubbox.model.common.BasicModel;

public class TblConfig extends BasicModel implements Serializable {
	private static final long serialVersionUID = -8801259518254286978L;
	/**
	 * 字典类别
	 */
	private String type;
	/**
	 * 字典值
	 */
	private String value;
	/**
	 * 标签
	 */
	private String label;
	/**
	 * 备注
	 */
	private String remark;
	/**
	 * 排序
	 */
	private Short sort;
	/**
	 * 创建人
	 */
	private String cuser;
	/**
	 * 修改人
	 */
	private String uuser;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public Short getSort() {
		return sort;
	}

	public void setSort(Short sort) {
		this.sort = sort;
	}

	public String getCuser() {
		return cuser;
	}

	public void setCuser(String cuser) {
		this.cuser = cuser;
	}

	public String getUuser() {
		return uuser;
	}

	public void setUuser(String uuser) {
		this.uuser = uuser;
	}

}