package com.wanma.dubbox.model;

import com.wanma.dubbox.model.common.BasicModel;

public class TblWebRelease extends BasicModel {
	private static final long serialVersionUID = 1L;
	private String ttl; // 发布标题
	private String indct; // 简介
	private String txt; // 发布内容
	private String upk; // 发布人
	private String type; // 发布类型（1活动中心2企业动态3行业动态）
	private Integer img;// 是否首图
	private Integer flg;// 是否有效
	private Integer ord;// 排序号
	private String imageUrl;

	public String getTtl() {
		return ttl;
	}

	public void setTtl(String ttl) {
		this.ttl = ttl;
	}

	public String getIndct() {
		return indct;
	}

	public void setIndct(String indct) {
		this.indct = indct;
	}

	public String getTxt() {
		return txt;
	}

	public void setTxt(String txt) {
		this.txt = txt;
	}
	
	public String getUpk() {
		return upk;
	}

	public void setUpk(String upk) {
		this.upk = upk;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public Integer getImg() {
		return img;
	}

	public void setImg(Integer img) {
		this.img = img;
	}

	public Integer getFlg() {
		return flg;
	}

	public void setFlg(Integer flg) {
		this.flg = flg;
	}

	public Integer getOrd() {
		return ord;
	}

	public void setOrd(Integer ord) {
		this.ord = ord;
	}

	public String getImageUrl() {
		return imageUrl;
	}

	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}

}
