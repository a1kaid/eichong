package com.appCore.model.conf;

import java.util.ArrayList;
import java.util.List;

import com.appCore.model.GameObject;


/**
 * 客户端配置
 * @author hao
 * Mar 18, 2014 11:58:33 AM
 */
public class ClientConfigs extends GameObject{
	
	//网关服务器配置
	private List<ClientConfig> gateServerConfigs = new ArrayList<ClientConfig>();
	//公共服务器配置（跨服战）
	private ClientConfig publicServerConfig;
	//世界服务器配置
	private ClientConfig worldServerConfig;
	

	public List<ClientConfig> getGateServerConfigs() {
		return gateServerConfigs;
	}
	public void setGateServerConfigs(List<ClientConfig> gateServerConfigs) {
		this.gateServerConfigs = gateServerConfigs;
	}
	public ClientConfig getPublicServerConfig() {
		return publicServerConfig;
	}
	public void setPublicServerConfig(ClientConfig publicServerConfig) {
		this.publicServerConfig = publicServerConfig;
	}
	public ClientConfig getWorldServerConfig() {
		return worldServerConfig;
	}
	public void setWorldServerConfig(ClientConfig worldServerConfig) {
		this.worldServerConfig = worldServerConfig;
	}
	
	
}
