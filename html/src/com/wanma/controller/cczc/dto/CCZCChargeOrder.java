public class CCZCChargeOrder {
    private String inOrderId;//桩企订单号
    private String outOrderId;//我方生成的订单号
    private String beginTime;//充电开始时间:yyyy-MM-dd HH:mm:s
    private String endTime;//充电结束时间:yyyy-MM-dd HH:mm:s
    private double power;//电量(单位:度)
    private double fee;//金额(单位:元,精确两位)

    public String getInOrderId() {
        return inOrderId;
    }

    public void setInOrderId(String inOrderId) {
        this.inOrderId = inOrderId;
    }

    public String getOutOrderId() {
        return outOrderId;
    }

    public void setOutOrderId(String outOrderId) {
        this.outOrderId = outOrderId;
    }

    public String getBeginTime() {
        return beginTime;
    }

    public void setBeginTime(String beginTime) {
        this.beginTime = beginTime;
    }

    public String getEndTime() {
        return endTime;
    }

    public void setEndTime(String endTime) {
        this.endTime = endTime;
    }

    public double getPower() {
        return power;
    }

    public void setPower(double power) {
        this.power = power;
    }

    public double getFee() {
        return fee;
    }

    public void setFee(double fee) {
        this.fee = fee;
    }
}
