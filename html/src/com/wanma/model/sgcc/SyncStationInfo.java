package com.wanma.model.sgcc;

import com.alibaba.fastjson.annotation.JSONField;

import java.math.BigDecimal;

/**
 * Created by zangyaoyi on 2017/6/5.
 * 同步充电站信息
 */
public class SyncStationInfo {
    private String StationID;//充电站ID
    private String OperatorID;//运营商ID,国网分配
    private String EquipmentOwnerID;//设备所属运营平台组织机构代码，国网分配
    private String StationName;//充电站名称
    private String CountryCode;//CN
    private String AreaCode;//省市辖区编码，参照GB/T2260-2015
    private String Address;//详细地址（不需要省市区）
    private String ServiceTel;//平台服务电话
    private int StationType;//站点类型
    private int StationStatus;//站点状态
    private int ParkNums;//车位数量
    private BigDecimal StationLng;//经度（GCJ-02坐标系）
    private BigDecimal StationLat;//GCJ-02坐标系
    private int Construction;//建设场所
    private String OpenForBusinessDate;//投入运营日期（yyyy-MM-dd）

    @JSONField(name = "StationID")
    public String getStationID() {
        return StationID;
    }

    public void setStationID(String stationID) {
        StationID = stationID;
    }

    @JSONField(name = "OperatorID")
    public String getOperatorID() {
        return OperatorID;
    }

    public void setOperatorID(String operatorID) {
        OperatorID = operatorID;
    }

    @JSONField(name = "EquipmentOwnerID")
    public String getEquipmentOwnerID() {
        return EquipmentOwnerID;
    }

    public void setEquipmentOwnerID(String equipmentOwnerID) {
        EquipmentOwnerID = equipmentOwnerID;
    }

    @JSONField(name = "StationName")
    public String getStationName() {
        return StationName;
    }

    public void setStationName(String stationName) {
        StationName = stationName;
    }

    @JSONField(name = "CountryCode")
    public String getCountryCode() {
        return CountryCode;
    }

    public void setCountryCode(String countryCode) {
        CountryCode = countryCode;
    }

    @JSONField(name = "AreaCode")
    public String getAreaCode() {
        return AreaCode;
    }

    public void setAreaCode(String areaCode) {
        AreaCode = areaCode;
    }

    @JSONField(name = "Address")
    public String getAddress() {
        return Address;
    }

    public void setAddress(String address) {
        Address = address;
    }

    @JSONField(name = "ServiceTel")
    public String getServiceTel() {
        return ServiceTel;
    }

    public void setServiceTel(String serviceTel) {
        ServiceTel = serviceTel;
    }

    @JSONField(name = "StationType")
    public int getStationType() {
        return StationType;
    }

    public void setStationType(int stationType) {
        StationType = stationType;
    }

    @JSONField(name = "StationStatus")
    public int getStationStatus() {
        return StationStatus;
    }

    public void setStationStatus(int stationStatus) {
        StationStatus = stationStatus;
    }

    @JSONField(name = "ParkNums")
    public int getParkNums() {
        return ParkNums;
    }

    public void setParkNums(int parkNums) {
        ParkNums = parkNums;
    }

    @JSONField(name = "StationLng")
    public BigDecimal getStationLng() {
        return StationLng;
    }

    public void setStationLng(BigDecimal stationLng) {
        StationLng = stationLng;
    }

    @JSONField(name = "StationLat")
    public BigDecimal getStationLat() {
        return StationLat;
    }

    public void setStationLat(BigDecimal stationLat) {
        StationLat = stationLat;
    }

    @JSONField(name = "Construction")
    public int getConstruction() {
        return Construction;
    }

    public void setConstruction(int construction) {
        Construction = construction;
    }

    @JSONField(name = "OpenForBusinessDate")
    public String getOpenForBusinessDate() {
        return OpenForBusinessDate;
    }

    public void setOpenForBusinessDate(String openForBusinessDate) {
        OpenForBusinessDate = openForBusinessDate;
    }
}
